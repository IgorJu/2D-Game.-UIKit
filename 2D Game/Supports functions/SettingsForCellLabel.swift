import UIKit

func settingsForCell() -> UILabel {
    let label = UILabel()
    label.translatesAutoresizingMaskIntoConstraints = false
    label.font = UIFont.systemFont(ofSize: 20.0)
    label.textColor = .black
    
    return label
}

